package com.matchaleaf.filesystem.dto;

public class FileDto {

	private Integer id;
	private String name;

	public FileDto() {

	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
